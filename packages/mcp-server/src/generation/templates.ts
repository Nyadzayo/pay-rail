function generateTsIdempotencyConfig(provider: string): string {
  return `/**
 * Idempotency configuration for ${pascalCase(provider)}
 *
 * Auto-generated by PayRail MCP generate_adapter tool.
 * Key format: {provider}:{merchantId}:webhook:{eventId}
 * TTL: 30 days — matches PayRail engine default.
 */
export const idempotencyConfig = {
  provider: "${provider}",
  keyFormat: "${provider}:{merchantId}:webhook:{eventId}",
  ttlSeconds: 30 * 24 * 60 * 60, // 30 days
  /**
   * Build idempotency key from webhook event data.
   * Fail-closed: if key cannot be built, defer processing rather than process unsafely.
   */
  buildKey(merchantId: string, eventId: string): string {
    if (!merchantId || !eventId) {
      throw new Error("[IDEMPOTENCY_KEY] Cannot build key: missing merchantId or eventId. Defer processing.");
    }
    return \`${provider}:\${merchantId}:webhook:\${eventId}\`;
  },
};
`;
}

export function generateAdapterFiles(
  provider: string,
  targetLanguage: "typescript" | "rust",
  facts: ClassifiedFacts,
  fingerprint: CodebaseFingerprint | null,
  config: PayRailConfig,
): GeneratedFile[] {
  if (targetLanguage === "rust") {
    // Rust generation is secondary — return stub with VERIFY markers
    return [{
      name: `${provider}_adapter.rs`,
      path: `src/adapters/${provider}_adapter.rs`,
      content: `// VERIFY: Rust adapter generation is experimental (confidence: 0.5, source: inferred, check: Review generated code carefully)\n// TODO: Implement Rust adapter for ${provider}\nunimplemented!()`,
      lineCount: 3,
    }];
  }

  const thresholds: GenerationThresholds = {
    generate: config.confidence.generate,
    verifyMin: config.confidence.verify_min,
  };
  const adapterContent = generateTsAdapter(provider, facts, fingerprint, thresholds);
  const webhookContent = generateTsWebhookHandler(provider, facts, fingerprint);
  const testContent = generateTsConformanceTests(provider, facts, fingerprint);
  const idempotencyContent = generateTsIdempotencyConfig(provider);

  const files: GeneratedFile[] = [
    {
      name: `${provider}-adapter.ts`,
      path: `src/adapters/${provider}-adapter.ts`,
      content: adapterContent,
      lineCount: adapterContent.split("\n").length,
    },
    {
      name: `${provider}-webhook.ts`,
      path: `src/adapters/${provider}-webhook.ts`,
      content: webhookContent,
      lineCount: webhookContent.split("\n").length,
    },
    {
      name: `${provider}-adapter.test.ts`,
      path: `src/adapters/${provider}-adapter.test.ts`,
      content: testContent,
      lineCount: testContent.split("\n").length,
    },
    {
      name: `${provider}-idempotency.ts`,
      path: `src/adapters/${provider}-idempotency.ts`,
      content: idempotencyContent,
      lineCount: idempotencyContent.split("\n").length,
    },
  ];

  return files;
}
